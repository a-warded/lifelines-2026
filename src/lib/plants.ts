// Utility functions
export function getPlantById(id: string): PlantData | undefined {
    return PLANTS[id];
}

export function getPlantsByCategory(category: PlantCategory): PlantData[] {
    return Object.values(PLANTS).filter((p) => p.category === category);
}

export function getPlantsByDifficulty(difficulty: Difficulty): PlantData[] {
    return Object.values(PLANTS).filter((p) => p.difficulty === difficulty);
}

export function getPlantsByWaterNeed(waterNeed: WaterNeed): PlantData[] {
    return Object.values(PLANTS).filter((p) => p.waterNeed === waterNeed);
}

export function getPlantsByGoal(goal: Goal): PlantData[] {
    return Object.values(PLANTS).filter((p) => p.goalFit.includes(goal));
}

export function getPlantsForSpace(space: GrowingSpace): PlantData[] {
    return Object.values(PLANTS).filter((p) => p.spaceEfficiency.includes(space));
}

export function getAllPlants(): PlantData[] {
    return Object.values(PLANTS);
}

export function getPlantByName(name: string): PlantData | undefined {
    const normalizedName = name.toLowerCase().trim();
    return Object.values(PLANTS).find((p) => 
        p.name.toLowerCase() === normalizedName ||
        p.id === normalizedName ||
        p.id === normalizedName.replace(/\s+/g, "-")
    );
}

export function getPlantOptions(): { value: string; label: string }[] {
    return Object.entries(PLANTS).map(([id, plant]) => ({
        value: id,
        label: plant.name,
    }));
}

// Calculate daily water for a plant at a specific stage
export function calculatePlantWater(
    plantId: string,
    stage: GrowthStage,
    plantCount: number
): number {
    const plant = PLANTS[plantId];
    if (!plant) return 0;
    return plant.waterByStage[stage] * plantCount;
}

